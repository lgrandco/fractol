int	ft_tolower(int c)
{
	return (c + (c >= 'A' && c <= 'Z') * 32);
}

char	*ft_lower(char *str)
{
	int	i;

	i = 0;
	while (*(str + i))
	{
		*(str + i) = ft_tolower(*(str + i));
		i++;
	}
	return (str);
}
