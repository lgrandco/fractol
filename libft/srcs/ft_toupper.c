int	ft_toupper(int c)
{
	return (c - (c >= 'a' && c <= 'z') * 32);
}

char	*ft_upper(char *str)
{
	int	i;

	i = 0;
	while (*(str + i))
	{
		*(str + i) = ft_toupper(*(str + i));
		i++;
	}
	return (str);
}
